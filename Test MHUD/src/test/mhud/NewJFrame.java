    public static String encryptmesg(String message, int key)
    {
        StringBuilder encryptedmesga = new StringBuilder();
        message = message.toLowerCase();
        
        for (int i = 0 ; i<message.length(); i++){
            char c = (message.charAt(i));
            if (Character.isLetter(c)){
                c = (char)((c - 'a' + key + 26) % 26 + 'a');
            }
            encryptedmesga.append(c);
        }
        return encryptedmesga.toString();
    }

